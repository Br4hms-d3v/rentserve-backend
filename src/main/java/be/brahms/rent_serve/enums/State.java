package be.brahms.rent_serve.enums;

/**
 * This enum represents different states of an object.
 */
public enum State {
    /**
     * The object is in good condition.
     */
    GOOD_STATE,
    /**
     * The object is in bad condition.
     */
    BAD_STATE,
    /**
     * The object is damaged.
     */
    DAMAGED_STATE
}
